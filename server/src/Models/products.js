const { Schema, model } = require("mongoose");

const ProductsSchema = Schema({
  id: Number,
  title: String,
  description: String,
  price: Number,
  discountPercentage: Number,
  rating: Number,
  stock: Number,
  brand: String,
  category: String,
  thumbnail: String,
  images: [String],
});

module.exports = model("Products", ProductsSchema);
