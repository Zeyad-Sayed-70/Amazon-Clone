const { Schema, model } = require("mongoose");

const UserAccountSchema = Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = model("UserAccount", UserAccountSchema);
